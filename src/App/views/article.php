              <div class="text__block margin--flex pb--medium">
                <p>
                  Lay-delivered mental health programmes such as the Friendship
                  Bench have a range of benefits, says Dr Nilofer Naqvi, an
                  academic psychologist who works in sub-Saharan Africa. They
                  also hold lessons for expanding mental healthcare access to
                  marginalised communities in the global north, including
                  Indigenous peoples.
                </p>
                <p>
                  “With mental health carrying so much cultural belief and
                  stigma, there are also genuine benefits in using
                  interventionists, such as these grandmothers, who have an
                  in-depth local, cultural knowledge and who are part of the
                  community,” she says.
                </p>
                <p>
                  Yet there are risks, she notes. “[These programmes] can let
                  health systems off the hook from investing in expert mental
                  healthcare.” Naqvi worries about the emergence of a pool of
                  lay mental healthcare providers in the global south “who are
                  not equipped for the burdens they face in an absence of
                  emergency psychiatry settings”, such as dealing with a violent
                  psychotic episode.
                </p>
                <p>
                  Chibanda admits that there have been mistakes in translating
                  projects to other contexts. While the Friendship Bench is
                  thriving in Zanzibar, where the national Ministry of Health is
                  scaling the project nationally, and in Vietnam with young
                  people with HIV, a project in Jordan was wound down after
                  government support failed to materialise, and in New York’s
                  Bronx and Harlem after a sympathetic local administration was
                  voted out. “Without local stakeholder buy-in we don’t have
                  sufficient capacity or indeed local knowledge,” he says.
                </p>
                <blockquote>
                  <p>
                    Grandmothers talk in the sessions about kuvhura pfungwa,
                    which is a Shona word for opening up the mind
                  </p>
                </blockquote>
                <p>
                  Friendship Bench packages as part of companies’ corporate
                  social responsibility provision – offered by companies
                  including Delta, First Mutual Life and Econet – have been
                  criticised for offering slimmed-down versions of the model in
                  settings not conducive to seeking mental health support: the
                  Friendship Bench lite.
                </p>
                <p>
                  Chibanda, however, is unfazed. “We have helped almost half a
                  million people in the past few years – and that’s actually not
                  enough when you think of the magnitude of the problem and the
                  care gap that exists,” he says, breaking off from negotiating
                  his next rank of benches in London. “Our vision is to have a
                  Friendship Bench within walking distance – everywhere.”
                </p>
                <p>
                  Mudzingwa, who is now sober and on good terms with his sons,
                  warmly encourages fellow Zimbabweans who are suffering
                  <em>kufungisisa</em> to give the Friendship Bench a try. “What
                  do you have to lose?” he asks.
                </p>
