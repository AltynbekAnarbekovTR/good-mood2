            <div class="article__content col--8--last col--12--bp3">
              <div class="intro__paragraph pb--medium">
                <p>
                  A legacy of poverty, genocide and dictatorship left Zimbabwe
                  struggling with an epidemic of depression, colloquially known
                  as kufungisisa, or ‘thinking too much’. Enter the Friendship
                  Bench: a landmark project that employs grandmothers to deliver
                  therapy in their neighbourhoods, which is coming to cities
                  around the world. Up next? London
                </p>
              </div>

              <div class="text__block margin--flex pb--medium">
                <p>
                  Would you like to share your story with me?” The elderly woman
                  in the long yellow dress softly enjoins her companion: a
                  slight man who sits with his head bowed towards his lap. It’s
                  springtime in Zimbabwe’s capital city, Harare, and the
                  setting, a simple wooden bench shaded by wild banana trees,
                  makes for an unconventional therapy room.
                </p>
                <p>
                  Known as ‘<em>gogos</em>’ (elder women) or ‘<em
                    >ambuya utano</em
                  >’ (community grannies), these Zimbabwean community health
                  workers (CHWs) have a record in treating mild to moderate
                  anxiety and depression that beats many traditional talking
                  therapies and pharmacological interventions. Meet the
                  Friendship Bench grandmothers of Zimbabwe.
                </p>
                <p>
                  Founded in 2007, the
                  <a
                    href="https://www.friendshipbenchzimbabwe.org/"
                    target="_blank"
                    rel="noopener"
                    >Friendship Bench project</a
                  >
                  has treated 280,000 people in its 16 years of existence, in 70
                  communities across Zimbabwe and at spin-off projects in
                  Malawi, Kenya and most recently Zanzibar and Vietnam.
                </p>
              </div>

              <div class="text__block margin--flex pb--medium">
                <p>
                  In 2024 it will arrive in London, with a series of Friendship
                  Benches set to be installed in the city’s most marginalised
                  communities.
                </p>
                <p>
                  “Whether it’s London, New York or Zimbabwe, everywhere the
                  issues are similar,” Friendship Bench founder, Harare-based
                  psychiatrist Dr Dixon Chibanda tells Positive News. “There are
                  issues related to loneliness, access to care, and to just
                  being able to know that what you’re experiencing – whether you
                  call it stress or depression or anxiety – is treatable.”
                </p>
                <p>
                  Were it not for the tragic death of a young woman called Erica
                  the project might never have come to pass. Late one evening in
                  2004, Chibanda received a call from a hospital doctor 100
                  miles south of his clinic in the capital: a 26-year-old who
                  Chibanda had treated at his Harare hospital a few months
                  earlier had attempted suicide. Chibanda talked to the young
                  woman’s mother via telephone and decided that as soon as she
                  was released from hospital, the pair would come to meet him to
                  re-evaluate Erica’s treatment plan. A week passed, and then
                  two more, with no word from the young woman. Finally, Chibanda
                  received a call from her distraught mother. Erica had killed
                  herself three days before.
                </p>

                <p>
                  “Why didn’t you come to Harare?” Chibanda recalls asking .“We
                  had agreed that as soon as she was released, you would come to
                  me…”
                </p>
                <p>
                  “We didn’t have the $15 bus fare to travel to Harare,” her
                  mother replied.
                </p>
                <p>
                  It was a lightbulb moment for the Zimbabwean hospital
                  psychiatrist, who had assumed that those in need of his help
                  would be able to access care at his city clinic. “I knew then
                  I needed to take psychiatry out of the hospital and into the
                  community,” he explains. Chibanda also needed to achieve this
                  feat in a country with a high level of stigma and superstition
                  around mental ill-health, in which 70 per cent of the
                  population live below the poverty line and where there were
                  just 13 psychiatrists in a nation of 16 million.
                </p>
              </div>

              <div class="image__block pb--medium margin--flex">
                <img
                  src="https://www.positive.news/wp-content/uploads/2023/07/5-e1690270049855-1500x0-c-default.jpg"
                  class="image--responsive"
                  alt="Zimbabwe"
                />

                <p class="img__caption pb--medium">
                  Juliet Kusikwenyu was one of the first Friendship Bench
                  grannies. She says that helping people improve their lives
                  "gives me strength to work until I am 80!"
                </p>
              </div>

              <div class="text__block margin--flex pb--medium">
                <p>
                  According to the World Health Organization (WHO), more than
                  300 million people suffer from depression globally. It is the
                  world’s leading cause of disability and contributes to 800,000
                  suicides per year, the majority in developing countries. In
                  2006, the year the Friendship Bench launched, Zimbabwe’s
                  suicide rate was 20.1 per 100,000 population, twice the
                  international average and one of the world’s worst records in
                  a nation that has suffered a series of population-level
                  traumas: from the five-year Gukurahundi genocide, to an HIV
                  pandemic that saw 25 per cent of the population infected with
                  the virus by 1997, and dictator Robert Mugabe’s 2005 campaign
                  to forcibly clear slums (Operation Murambatsvina – ‘remove the
                  filth’), which left 700,000 Zimbabweans homeless.
                </p>
                <p>
                  Elton Mudzingwa, 47, had hit rock bottom when his neighbour
                  suggested he attend grandmother Felistas Gasa’s Friendship
                  Bench in Harare. The street vendor had lost his wife and was
                  struggling to cope with his grief at the same time as raising
                  two sons alone. With his youngest son slipping into street
                  drug addiction, father and son were no longer speaking, and
                  Mudzingwa had begun to drown his sorrows with cheap beer.
                  Mudzingwa arrived at his first session drunk, he admits.
                </p>
                <p>
                  “But grandmother was patient enough to speak with me,” he
                  says. “She asked about my state of mind and if I was
                  entertaining any suicidal thoughts.” As Gasa was a friendly
                  neighbourhood face, Mudzingwa felt emboldened to open up. “I
                  felt as if she cared for me, she was not telling me what to do
                  or being judgmental,” he explains.
                </p>
                <p>
                  Gasa beams. “In the early days [of Friendship Bench], many of
                  the people who came to us were stranded on the streets with
                  their children [due to Operation Murambatsvina],” she says.
                  “My work then and now was about changing lives as much as
                  changing the way people look at their lives: a homeless woman
                  might need advice on how to get funding to start a small
                  business so she can return to her village with her children,
                  for example.” Gasa feels happy when she sees lives change. “I
                  think to myself: what if I had just ignored their story. Where
                  would they be?” she adds.
                </p>
              </div>

              <div class="image__block pb--medium margin--flex">
                <img
