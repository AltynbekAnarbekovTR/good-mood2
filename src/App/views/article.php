                <p class="img__caption pb--medium">
                  Juliet Kusikwenyu counselling client Vimbai Madziwa. She says:
                  "My husband is a drunkard and I often feared my four children
                  would die as they were so often hospitalised with illnesses
                  due to poverty. These sessions have given me the confidence to
                  get work to feed my family myself."
                </p>
              </div>

              <div class="text__block margin--flex pb--medium">
                <p>
                  The Friendship Bench method begins with talking therapy. Most
                  of the therapists are older women, who are traditionally
                  turned to for counsel in Zimbabwean culture. The women are
                  trained in the basics of cognitive behavioural therapy [CBT]
                  and allocated a park bench in their communities. They are paid
                  a wage by the Zimbabwean Ministry of Health [equivalent to a
                  community health worker’s wage], topped up by money fundraised
                  by Friendship Bench. Referrals to the service, which come via
                  primary healthcare, schools and police stations, and more
                  recently self-referrals via social media, are screened via a
                  validated questionnaire that’s couched in Zimbabwean concepts
                  of mental ill-health: the Shona Symptom Questionnaire.
                  Clinical cases, such as those at risk of suicide, are referred
                  to hospital settings, with selected cases going on to receive
                  two to four bench sessions. After these, clients are invited
                  to join a peer support group for those who have received the
                  Friendship Bench therapy (a circle <em>kubatana tose</em> or
                  ‘holding hands together’ group), at which attendees are taught
                  revenue-generating skills, such as making bags out of recycled
                  materials.
                </p>
                <p>
                  Grandmother Juliet Kusikwenyu, 70 and based in Mbare [south of
                  Harare], was recruited to the Friendship Bench from her job as
                  a health visitor. It was Dixon Chibanda’s enthusiasm for
                  community based mental healthcare that attracted her.
                </p>
                <p>
                  “He said that people are suffering from headaches and other
                  symptoms but that the cause will be depression or stress,”
                  Kusikwenyu says. “This made sense to me, and I wanted to
                  help.”
                </p>
                <blockquote>
                  <p>
                    Helping people solve their problems brings me so much joy
                  </p>
                </blockquote>
                <p>
                  Integral to the Friendship Bench method is its articulation
                  through local cultural concepts rather than western medical
                  diagnoses, which can contribute to stigma. “Grandmothers talk
                  in the sessions about <em>kuvhura pfungwa</em>, which is a
                  Shona word for ‘opening up the mind’,” Chibanda explains,
                  “they also talk about <em>kusimudzira</em>, a Shona word for
                  ‘uplifting’ and <em>kusimbisa</em>, which means
                  ‘strengthening’.”
                </p>
                <p>
                  Indeed the term ‘Friendship Bench’ was suggested by the
                  project’s founding grandmothers, Chibanda adds. “Initially we
                  called it the Mental Health Bench, but the grandmothers told
                  me no one would want to come to a project with such a
                  stigmatising name,” he says.
                </p>
                <p>
                  Kusikwenyu’s most memorable client was a young man called
                  Samson, who came to the bench after finding out he was HIV
                  positive. His diagnosis led to separation from his wife and he
                  was left with three children and his elderly mother who had
                  angrily rejected her son due to his HIV.
                </p>
              </div>

              <div class="image__block pb--medium margin--flex">
                <img
                  src="https://www.positive.news/wp-content/uploads/2023/07/3-3-1500x0-c-default.jpg"
                  class="image--responsive"
                  alt="Elton Mudzingwa, a street vendor who struggled to cope after his wife died, in Zimbabwe"
                />

                <p class="img__caption pb--medium">
                  Street vendor Elton Mudzingwa, who struggled to cope after his
                  wife died. With the grandmothers help, he has quit drinking.
                  "I felt as if she cared for me, she was not telling me what to
                  do or being judgemental," he says.
                </p>
              </div>

              <div class="text__block margin--flex pb--medium">
                <p>
                  “I encouraged Samson and his mother to come to the bench
                  together so they could both accept his [HIV] status,”
                  Kusikwenyu says. “Then I urged him to get treatment for his
                  condition and, when he started taking his medication, we
                  looked at his stresses around providing for his three
                  children.” With the grandmother’s counsel, Samson borrowed
                  seed money to start a vegetable vending business. His business
                  began to grow and today Samson is happily remarried to a woman
                  he met at church who is also living with HIV. Now a tenant
                  farmer, Samson has built a house for his family. “Stories like
                  this bring me so much joy,” says Kusikwenyu. “It gives me the
                  strength to work till I am 80!”
                </p>
                <p>
                  In 2016, Chibanda – collaborating with colleagues from
                  Zimbabwe and the UK – published the results of a
                  six-month-long randomised control trial in the Journal of the
                  American Medical Association. They split 600 people with
                  symptoms of depression into two groups and found that the
                  group that saw the grandmothers had significantly lower
                  symptoms of depression compared to the group that had
                  conventional treatment.
                </p>
                <p>
                  To date there have been more than 100 peer-reviewed
                  publications on the Friendship Bench, including
                  <a
                    href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC8392686/"
                    target="_blank"
                    rel="noopener"
                    >a 2021 study</a
                  >
                  which found that 67 per cent of women clients in Zimbabwe had
                  maintained or expanded their income-generating projects, and
                  another that found broad benefits in the model for
                  grandmothers’ own mental health. An upcoming study, meanwhile,
                  finds that the Friendship Bench improves the health outcomes
                  of clients with diabetes and hypertension.
                </p>
              </div>

