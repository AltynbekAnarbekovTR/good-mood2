              <div class="text__block margin--flex pb--medium">
                <p>
                  According to the World Health Organization (WHO), more than
                  300 million people suffer from depression globally. It is the
                  world’s leading cause of disability and contributes to 800,000
                  suicides per year, the majority in developing countries. In
                  2006, the year the Friendship Bench launched, Zimbabwe’s
                  suicide rate was 20.1 per 100,000 population, twice the
                  international average and one of the world’s worst records in
                  a nation that has suffered a series of population-level
                  traumas: from the five-year Gukurahundi genocide, to an HIV
                  pandemic that saw 25 per cent of the population infected with
                  the virus by 1997, and dictator Robert Mugabe’s 2005 campaign
                  to forcibly clear slums (Operation Murambatsvina – ‘remove the
                  filth’), which left 700,000 Zimbabweans homeless.
                </p>
                <p>
                  Elton Mudzingwa, 47, had hit rock bottom when his neighbour
                  suggested he attend grandmother Felistas Gasa’s Friendship
                  Bench in Harare. The street vendor had lost his wife and was
                  struggling to cope with his grief at the same time as raising
                  two sons alone. With his youngest son slipping into street
                  drug addiction, father and son were no longer speaking, and
                  Mudzingwa had begun to drown his sorrows with cheap beer.
                  Mudzingwa arrived at his first session drunk, he admits.
                </p>
                <p>
                  “But grandmother was patient enough to speak with me,” he
                  says. “She asked about my state of mind and if I was
                  entertaining any suicidal thoughts.” As Gasa was a friendly
                  neighbourhood face, Mudzingwa felt emboldened to open up. “I
                  felt as if she cared for me, she was not telling me what to do
                  or being judgmental,” he explains.
                </p>
                <p>
                  Gasa beams. “In the early days [of Friendship Bench], many of
                  the people who came to us were stranded on the streets with
                  their children [due to Operation Murambatsvina],” she says.
                  “My work then and now was about changing lives as much as
                  changing the way people look at their lives: a homeless woman
                  might need advice on how to get funding to start a small
                  business so she can return to her village with her children,
                  for example.” Gasa feels happy when she sees lives change. “I
                  think to myself: what if I had just ignored their story. Where
                  would they be?” she adds.
                </p>
              </div>

